# vi 편집기로 파일 열기
 vi 파일명

# 입력 모드로 전환
  a : 다음 커서부터 입력
  i : 현재 커서에서 입력
  o : 다음 줄부터 입력

# 명령모드 전환 : esc (습관적으로 사용)
 x : 한 칸 삭제
 dd : 한 줄 삭제
 yy : 한 줄 복사
 p : 한 줄 다음 줄에 붙여넣기
 G : 파일의 마지막 줄로 이동
 gg : 파일의 첫번째 줄로 이동
 
 # 검색 시에
 /문자열 (검색하고자 하는)
 n : 검색된 결과의 다음 결과

 # vi 종료 시
 wq! : 저장 후 종료
 q! : 저장 없이 종료
